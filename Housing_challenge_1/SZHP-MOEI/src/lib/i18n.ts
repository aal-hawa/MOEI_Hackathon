// ─── Build lookup objects from the table ────────────────────────────────
function buildLookup(lang: 'en' | 'ar'): Record<string, string> {
  const lookup: Record<string, string> = {}
  for (const row of translationTable) {
    lookup[row.id] = row[lang] || row.en // fallback to English
  }
  return lookup
}

const enLookup = buildLookup('en')
const arLookup = buildLookup('ar')

export const translations = { en: enLookup, ar: arLookup } as const
export type TranslationKey = keyof typeof enLookup

/**
 * Translate a key to the given language
 * Falls back to English if the key is not found in the given language
 * Falls back to the key itself if not found in English either
 */
export function t(key: string, lang: Language = 'ar', params?: Record<string, string | number>): string {
  let text = (lang === 'ar' ? arLookup : enLookup)[key] || enLookup[key] || key
  if (params) {
    Object.entries(params).forEach(([k, v]) => {
      text = text.replace(`{${k}}`, String(v))
    })
  }
  return text
}

