const translationsMap: Record<Language, TranslationMap> = { en, ar };

/**
 * Retrieve the translation for a given key in the specified language.
 * Falls back to the key itself if no translation is found.
 * Supports simple interpolation: e.g. t('profile.fieldCount', { found: 5, total: 10 })
 * is NOT handled here – use the LanguageContext's `t()` function for that.
 */
export function getTranslation(lang: Language, key: string): string {
  return translationsMap[lang]?.[key] ?? translationsMap.en?.[key] ?? key;
}

/**
 * Get all translations for a given language.
 */
export function getTranslations(lang: Language): TranslationMap {
  return translationsMap[lang] ?? translationsMap.en;
}

